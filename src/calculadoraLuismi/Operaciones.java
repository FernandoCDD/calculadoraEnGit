package calculadoraLuismi;

public class Operaciones {

	public double sumar (double numer, double denom){
		
		return numer + denom;
	}
	
	public double restar (double numer, double denom){
		
		return numer - denom;
	}
	
	public double multiplicar (double numer, double denom){
		
		return numer * denom;
	}
}
